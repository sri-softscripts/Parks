            <h2>ENGAGE VISITORS</h2>
            <p>
              Kurt Fristrup, the Science and Technology Branch Chief of the
              Natural Sounds and Night Skies Division of the NPS, believes that
              encouraging visitors to attentively engage soundscapes in parks
              can enrich their experiences (Selleck & KellerLynn, 2010). Some
              studies show that over 90% of visitors find the opportunity to
              experience natural quiet and the sounds of nature to be compelling
              reasons to visit parks (McDonals et al., 1995)
            </p>
            <p>
              In Glacier National Park, interpretive signs help encourage
              visitors on the Trail of the Cedars to enjoy the natural sounds of
              the trail.
            </p>
