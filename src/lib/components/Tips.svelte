            <h2>FOLLOW MANAGEMENT-BY-OBJECTIVES FRAMEWORK</h2>
            <p>
              Frameworks, like the Visitor Experience and Resource Protection
              and Visitor Use Management frameworks, can help establish
              successfully manage soundscape resources by using a five-step
              process. Here is how:
            </p>
            <ol class="list">
              <li>
                <strong>Establish management objectives</strong><br />These
                broad narrative statements describe the desired conditions of
                park resources and experiences.
              </li>
              <li>
                <strong>Identify indicators and thresholds</strong><br
                />Indicators are measurable and manageable variables that relate
                to your management objectives. Thresholds are the range of
                acceptable conditions for an indicator and allow managers to
                understand how observed conditions related to management
                objectives.
              </li>
              <li>
                <strong>Monitor the indicators</strong><br />Gathering data from
                the indicators helps you describe current conditions and
                understand what needs to be done to achieve your managment
                objectives.
              </li>
              <li>
                <strong>Implement management actions</strong><br />Armed with an
                understanding of current conditions and thresholds regarding the
                indicators, management can be enacted in an adaptive process to
                help achieve management goals.
              </li>
              <li>
                <strong>Repeat steps 4 and 5</strong><br />Periodic monitoring
                will allow you to understand the effect of your management
                actions, and whether new or additional actions need to be
                implemented.
              </li>
            </ol>
            <p>
              Dr. Peter Newman has conducted research in parks using
              management-by-objectives frameworks for over XX years. According
              to Dr. Newman:
            </p>
            <div class="content-quote">
              “Management-by-objective” frameworks institutionalize science
              based decision making where managers set desired conditions,
              create indicators and standards of quality and use science based
              approaches to measure change and efficacy of management actions.
              This process allows agencies to learn and adapt to rapidly
              changing and complex human-ecological systems.”
            </div>
