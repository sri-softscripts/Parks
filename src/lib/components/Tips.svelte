            <h2>USE ZONING</h2>
            <p>
              Parks are often not homogenous places. Instead, they provide a
              variety of different recreation opportunities for visitors. Zoning
              can help managers focus on placing limited resource in strategic
              areas for reducing noise emissions. Zoning is not only spatial,
              but also temporal.
            </p>
            <p>
              Take for example Muir Woods National Monument. In this park,
              managers implemented both “quiet zones” and “quiet days” which
              reflect the spatial and temporal aspects of zoning. (Stack et al.,
              2011)
            </p>
